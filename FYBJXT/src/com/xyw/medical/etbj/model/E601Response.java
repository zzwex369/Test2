package com.xyw.medical.etbj.model;

import java.util.List;
import java.util.Map;

import com.xyw.sys.model.MessageResponse;

public class E601Response extends MessageResponse {
	private List<VE601> ve601s;
	private long rowCount;
	private boolean state;
	private Map<String, Object>e601Map;
	
	
	public Map<String, Object> getE601Map() {
		return e601Map;
	}
	public void setE601Map(Map<String, Object> e601Map) {
		this.e601Map = e601Map;
	}
	public List<VE601> getVe601s() {
		return ve601s;
	}
	public void setVe601s(List<VE601> ve601s) {
		this.ve601s = ve601s;
	}
	public long getRowCount() {
		return rowCount;
	}
	public void setRowCount(long rowCount) {
		this.rowCount = rowCount;
	}
	public boolean isState() {
		return state;
	}
	public void setState(boolean state) {
		this.state = state;
	}
	
}
