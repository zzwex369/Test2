package com.xyw.medical.etbj.etsl.model;

import java.util.List;

import com.xyw.sys.model.MessageResponse;

public class E703Response extends MessageResponse{

	private boolean falg;
	/**
	 * 1 重复数据
	 * 2出现异常
	 * 3没有数据
	 * 4已转诊无法修改
	 */
	private int state;
	
	private VE703 ve703;
	private List<VE703> ve703s;
	private long rowCount;

	
	
	

	public VE703 getVe703() {
		return ve703;
	}

	public void setVe703(VE703 ve703) {
		this.ve703 = ve703;
	}

	public List<VE703> getVe703s() {
		return ve703s;
	}

	public void setVe703s(List<VE703> ve703s) {
		this.ve703s = ve703s;
	}

	public boolean isFalg() {
		return falg;
	}

	public void setFalg(boolean falg) {
		this.falg = falg;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public long getRowCount() {
		return rowCount;
	}

	public void setRowCount(long rowCount) {
		this.rowCount = rowCount;
	}
	
	
}
